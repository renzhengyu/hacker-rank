def formingMagicSquare(s):
    c=[[[4,9,2],[3,5,7],[8,1,6]],
    [[2,7,6],[9,5,1],[4,3,8]],
    [[6,1,8],[7,5,3],[2,9,4]],
    [[8,3,4],[1,5,9],[6,7,2]],
    [[2,9,4],[7,5,3],[6,1,8]],
    [[6,7,2],[1,5,9],[8,3,4]],
    [[8,1,6],[3,5,7],[4,9,2]],
    [[4,3,8],[9,5,1],[2,7,6]]]
    result = 10000
    for m in c:
        sum=0
        for i in range(3):
            for j in range(3):
                sum+=abs(s[i][j]-m[i][j])
        if sum<result:
            result=sum
    return result